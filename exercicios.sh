Exercícios com Loops
Menu de Cores
Crie um menu onde o usuário pode adicionar uma cor, listar todas as cores ou sair.
Adicione uma funcionalidade para mostrar quantas vezes cada cor foi adicionada.
Contador de Vogais
====================================================================================================================================
Crie um programa onde o usuário insere uma palavra.
O programa deve contar quantas vogais existem nessa palavra e exibir o resultado.
Tabuada
====================================================================================================================================
Peça ao usuário para inserir um número.
Exiba a tabuada desse número (de 1 a 10) utilizando um loop.
Exercícios com Funções
Calculadora Simples
====================================================================================================================================
Crie uma função chamada calculadora que receba dois números e uma operação (soma, subtração, multiplicação ou divisão).
O programa deve chamar a função e exibir o resultado.
Filtro de Números Pares
====================================================================================================================================
Crie uma função que receba um array de números e retorne apenas os números pares.
Fatorial
====================================================================================================================================
Crie uma função chamada fatorial que receba um número e retorne o fatorial dele (n! = n × (n-1) × ... × 1).
Exercícios Avançados
Gerenciador de Tarefas
====================================================================================================================================
Crie um programa que funcione como um gerenciador de tarefas:
Adicionar uma tarefa.
Listar todas as tarefas.
Marcar uma tarefa como concluída.
Remover uma tarefa.
Sistema de Votação
====================================================================================================================================
Crie um programa onde o usuário possa votar em 3 candidatos diferentes.
O programa deve exibir o resultado com o número total de votos para cada candidato e o vencedor.
Jogo de Adivinhação
====================================================================================================================================
Crie um jogo onde o computador escolhe um número aleatório entre 1 e 100.
O usuário deve tentar adivinhar o número.
Diga se o palpite é maior ou menor que o número sorteado até o jogador acertar.
